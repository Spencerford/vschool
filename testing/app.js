var cal = {
    add: function (num1, num2) {
        return num1 + num2;
    },

    sub: function (num1, num2) {
        return num1 - num2;
    },

    mult: function (num1, num2) {
        return num1 * num2;
    },
    divide: function (num1, num2) {
        return num1 / num2;
    }

}

module.exports = cal;
